package pert5;

public class son2{
	private String name;
	private Integer qty,age;
	public son2(String name, Integer qty, Integer age) {
		this.name = name;
		this.age=age;
		this.qty=qty;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getQty() {
		return qty;
	}
	public void setQty(Integer qty) {
		this.qty = qty;
	}
	public Integer getAge() {
		return age;
	}
	public void setAge(Integer age) {
		this.age = age;
	}
	
}
